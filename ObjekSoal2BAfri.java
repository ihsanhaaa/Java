/**
 * ObjekSoal2BAfri
 */
public class ObjekSoal2BAfri {
    int saldowal, persenprofit, hslprofit, saldokhir;

    public ObjekSoal2BAfri(int saldoAwal, int persentaseProfit, int profit, int saldoAkhir) {
        saldowal = saldoAwal ;
        persenprofit = persentaseProfit ;
        hslprofit = profit ;
        saldokhir = saldoAkhir ;
    }

    /**
     * @return the saldowal
     */
    public int getSaldowal() {
        return saldowal;
    }

    /**
     * @param saldowal the saldowal to set
     */
    public void setSaldowal(int saldowal) {
        this.saldowal = saldowal;
    }

    /**
     * @return the persenprofit
     */
    public int getPersenprofit() {
        return persenprofit;
    }

    /**
     * @param persenprofit the persenprofit to set
     */
    public void setPersenprofit(int persenprofit) {
        this.persenprofit = persenprofit;
    }

    /**
     * @return the hslprofit
     */
    public int getHslprofit() {
        return hslprofit;
    }

    /**
     * @param hslprofit the hslprofit to set
     */
    public void setHslprofit(int hslprofit) {
        this.hslprofit = hslprofit;
    }

    /**
     * @return the saldokhir
     */
    public int getSaldokhir() {
        return saldokhir;
    }

    /**
     * @param saldokhir the saldokhir to set
     */
    public void setSaldokhir(int saldokhir) {
        this.saldokhir = saldokhir;
    }
}

    