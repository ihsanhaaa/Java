/**
 * ObjekSoal2B
 */
public class ObjekSoal2B {

    int saldoAwal, profitPersen, profit, saldoAkhir;

    public ObjekSoal2B(int saldoAwal, int profitPersen, int profit, int saldoAkhir) {
        this.saldoAwal = saldoAwal;
        this.profitPersen = profitPersen;
        this.profit = profit;
        this.saldoAkhir = saldoAkhir;
    }

    /**
     * @return the saldoAwal
     */
    public int getSaldoAwal() {
        return saldoAwal;
    }

    /**
     * @param saldoAwal the saldoAwal to set
     */
    public void setSaldoAwal(int saldoAwal) {
        this.saldoAwal = saldoAwal;
    }

    /**
     * @return the profitPersen
     */
    public int getProfitPersen() {
        return profitPersen;
    }

    /**
     * @param profitPersen the profitPersen to set
     */
    public void setProfitPersen(int profitPersen) {
        this.profitPersen = profitPersen;
    }

    /**
     * @return the profit
     */
    public int getProfit() {
        return profit;
    }

    /**
     * @param profit the profit to set
     */
    public void setProfit(int profit) {
        this.profit = profit;
    }

    /**
     * @return the saldoAkhir
     */
    public int getSaldoAkhir() {
        return saldoAkhir;
    }

    /**
     * @param saldoAkhir the saldoAkhir to set
     */
    public void setSaldoAkhir(int saldoAkhir) {
        this.saldoAkhir = saldoAkhir;
    }
}